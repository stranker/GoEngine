#include "Light.h"

void Light::Draw() {
    if (material){
        material->Use();
        material->SetMat4("mvp", renderer->GetCamera()->GetMVPOf(transform->GetTransform()));
    }
    renderer->Draw(GetVertexArrayID(), primitive, drawVertices, false);
    DrawGizmo();
}

